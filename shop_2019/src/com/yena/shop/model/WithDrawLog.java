package com.yena.shop.model;

public class WithDrawLog {
	private int withdraw_log_id;
	private String withdraw_cd;
	private String id;
	private String withdraw_dtm;
	
	
	public int getWithdraw_log_id() {
		return withdraw_log_id;
	}
	public void setWithdraw_log_id(int withdraw_log_id) {
		this.withdraw_log_id = withdraw_log_id;
	}
	public String getWithdraw_cd() {
		return withdraw_cd;
	}
	public void setWithdraw_cd(String withdraw_cd) {
		this.withdraw_cd = withdraw_cd;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getWithdraw_dtm() {
		return withdraw_dtm;
	}
	public void setWithdraw_dtm(String withdraw_dtm) {
		this.withdraw_dtm = withdraw_dtm;
	}
	
	
}
