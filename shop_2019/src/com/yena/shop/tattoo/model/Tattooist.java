package com.yena.shop.tattoo.model;

public class Tattooist {
	private int	SEQ;
	private String NAME;
	private int CAREER;
	private String REQ_DTM;
	public int getSEQ() {
		return SEQ;
	}
	public void setSEQ(int sEQ) {
		SEQ = sEQ;
	}
	public String getNAME() {
		return NAME;
	}
	public void setNAME(String nAME) {
		NAME = nAME;
	}
	public int getCAREER() {
		return CAREER;
	}
	public void setCAREER(int cAREER) {
		CAREER = cAREER;
	}
	public String getREQ_DTM() {
		return REQ_DTM;
	}
	public void setREQ_DTM(String rEQ_DTM) {
		REQ_DTM = rEQ_DTM;
	}
	
	
	
}
